        /// <summary>
        /// 加锁处理
        /// </summary>
        /// <param name="lockkey"></param>
        /// <param name="ac"></param>
        /// <returns></returns>
        public bool StringLockToDo(string lockkey, Action<IDatabase> ac)
        {
            return Do<bool>((IDatabase db) =>
            {
                bool flag = false;
                //设置timespan避免死锁
                if (db.LockTake(lockkey, Token, TimeSpan.FromSeconds(5)))
                {
                    try
                    {
                        ac(db);
                        flag = true;
                    }
                    finally
                    {
                        db.LockRelease(lockkey, Token);
                    }
                }
                return flag;
            });

        }

